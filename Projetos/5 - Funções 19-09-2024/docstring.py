def area_do_triangulo(base, altura):
    '''
        Função que calcula a área do triangulo.
        Params:
            - base : valor numérico float ou int
            - altura : valor numérico float ou int
        Returns:
            - valor numérico float
    '''
    area = base * altura / 2
    return area

